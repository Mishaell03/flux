CREATE TABLE users (
    user_id INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1000000) PRIMARY KEY,
    name VARCHAR(64),
    phone VARCHAR(50) UNIQUE,
    email VARCHAR(255) UNIQUE,
    img TEXT,
    img_provider VARCHAR(20) REFERENCES providers(code) ON DELETE CASCADE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX ix_users_phone ON users(phone);